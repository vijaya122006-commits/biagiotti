def _has_sentiment_labels(df_reviews: pd.DataFrame) -> Tuple[bool, Optional[str]]:
    """
    Check whether the reviews DataFrame contains usable sentiment labels.

    Returns
    -------
    (True, column_name)  if explicit sentiment column found,
    (False, None)        otherwise.
    """
    # Only columns whose values actually look like sentiment labels
    SENTIMENT_VALUES = {"positive", "negative", "neutral", "pos", "neg", "neu",
                        "good", "bad", "5", "4", "3", "2", "1"}
    candidates = ["sentiment", "sentiment_label", "review_sentiment"]
    for col in candidates:
        if col in df_reviews.columns:
            vals = set(str(v).strip().lower() for v in df_reviews[col].dropna().unique())
            if len(vals) >= 2 and vals.issubset(SENTIMENT_VALUES | {"mixed", "very positive", "very negative"}):
                logger.info(f"  Found sentiment label column: '{col}' ({len(vals)} unique values)")
                return True, col
    # Check 'label' only if its values overlap with sentiment vocabulary
    if "label" in df_reviews.columns:
        vals = set(str(v).strip().lower() for v in df_reviews["label"].dropna().unique())
        if vals.issubset(SENTIMENT_VALUES | {"mixed"}):
            logger.info("  Found sentiment labels in 'label' column.")
            return True, "label"
    logger.info("  No true sentiment label column found — will use VADER.")
    return False, None


def _vader_label(compound: float) -> str:
    """Convert a VADER compound score to a sentiment string."""
    if compound >= 0.05:
        return "positive"
    elif compound <= -0.05:
        return "negative"
    return "neutral"


