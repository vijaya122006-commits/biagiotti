def _make_sales_ts(df_sales: pd.DataFrame, freq: str = "W") -> pd.DataFrame:
    """
    Aggregate sales data into a time series at the given frequency.
    Returns a DataFrame with DatetimeIndex and per-product columns.

    Parameters
    ----------
    df_sales : master_sales DataFrame.
    freq     : Pandas offset alias ('W' = weekly, 'D' = daily, 'M' = monthly).

    Returns
    -------
    pd.DataFrame  — wide format: rows = dates, columns = product_id.
    """
    # Detect date column
    date_col = None
    for col in ["start_date", "order_date", "date", "date_published"]:
        if col in df_sales.columns:
            date_col = col
            break

    qty_col = None
    for col in ["units_sold", "sales", "venda", "revenues"]:
        if col in df_sales.columns:
            qty_col = col
            break

    pid_col = "product_id" if "product_id" in df_sales.columns else None

    if date_col is None or qty_col is None:
        logger.warning("  Could not identify date or quantity column in sales data.")
        return pd.DataFrame()

    sub = df_sales[[date_col, pid_col, qty_col]].copy() if pid_col else df_sales[[date_col, qty_col]].copy()
    sub[date_col] = pd.to_datetime(sub[date_col], errors="coerce")
    sub[qty_col]  = pd.to_numeric(sub[qty_col], errors="coerce")
    sub = sub.dropna(subset=[date_col, qty_col])
    sub = sub[sub[qty_col] >= 0]

    if pid_col:
        ts = (
            sub.groupby([pd.Grouper(key=date_col, freq=freq), pid_col])[qty_col]
            .sum()
            .unstack(fill_value=0)
        )
    else:
        ts = sub.groupby(pd.Grouper(key=date_col, freq=freq))[qty_col].sum().to_frame()

    return ts.sort_index()


def train_arima(df_sales: pd.DataFrame, top_n: int = 5) -> dict:
    """
    Fit ARIMA models for the top-N products by total sales.

    Parameters
    ----------
    df_sales : master_sales DataFrame.
    top_n    : Number of top products to fit ARIMA for.

    Returns
    -------
    dict with RMSE, MAE per product and combined metrics.
    """
    log_section("MODEL 5A — ARIMA Demand Forecasting")
    t0 = time.time()

    try:
        from statsmodels.tsa.arima.model import ARIMA
    except ImportError:
        logger.error("  statsmodels not installed — ARIMA skipped.")
        return {"error": "statsmodels missing"}

    ts = _make_sales_ts(df_sales, freq="W")
    if ts.empty:
        logger.warning("  Could not build time series — ARIMA skipped.")
        save_model({"error": "no time series"}, "arima_model.pkl", "ARIMA Model (empty)")
        return {}

    # Select top-N products
    if isinstance(ts.columns, pd.MultiIndex) or ts.shape[1] > 1:
        top_products = ts.sum().nlargest(top_n).index.tolist()
    else:
        top_products = ts.columns.tolist()

    logger.info(f"  Time series shape : {ts.shape}")
    logger.info(f"  Date range        : {ts.index.min()} → {ts.index.max()}")
    logger.info(f"  Fitting ARIMA for top-{len(top_products)} products …")

    arima_models: dict = {}
    all_rmse: List[float] = []
    all_mae:  List[float] = []

    for pid in top_products:
        try:
            product_ts = ts[pid].dropna()
            if len(product_ts) < 12:
                logger.warning(f"    {pid}: too few observations ({len(product_ts)}) — skipping")
                continue

            # Simple train/test split
            split = max(4, int(len(product_ts) * 0.8))
            train_ts = product_ts.iloc[:split]
            test_ts  = product_ts.iloc[split:]

            model = ARIMA(train_ts, order=(1, 1, 1))
            fitted = model.fit()

            if len(test_ts) > 0:
                forecasts = fitted.forecast(steps=len(test_ts))
                rmse = float(np.sqrt(mean_squared_error(test_ts, forecasts)))
                mae  = float(np.mean(np.abs(test_ts.values - forecasts.values)))
                all_rmse.append(rmse)
                all_mae.append(mae)
                logger.info(f"    {str(pid)[:20]:20s}  RMSE={rmse:8.2f}  MAE={mae:8.2f}  n={len(product_ts)}")
            else:
                logger.info(f"    {str(pid)[:20]:20s}  n={len(product_ts)} (no test split)")

            arima_models[str(pid)] = fitted
        except Exception as exc:
            logger.warning(f"    {pid}: ARIMA failed — {exc}")

    mean_rmse = float(np.mean(all_rmse)) if all_rmse else None
    mean_mae  = float(np.mean(all_mae))  if all_mae  else None

    logger.info(f"\n  ── ARIMA Summary ({len(arima_models)} models) ──")
    if mean_rmse:
        logger.info(f"  Mean RMSE : {mean_rmse:.2f}")
        logger.info(f"  Mean MAE  : {mean_mae:.2f}")

    save_model(
        {"models": arima_models, "top_products": [str(p) for p in top_products]},
        "arima_model.pkl",
        "ARIMA Demand Forecast Models",
    )

    metrics = {
        "n_products":  len(arima_models),
        "mean_rmse":   round(mean_rmse, 4) if mean_rmse else None,
        "mean_mae":    round(mean_mae, 4)  if mean_mae  else None,
        "elapsed":     elapsed(t0),
    }
    logger.info(f"\n  ✔ ARIMA complete  ({elapsed(t0)})")
    return metrics


