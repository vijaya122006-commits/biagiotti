def clean_text(series: pd.Series) -> pd.Series:
    """
    Basic text normalisation: lower case, strip whitespace, replace NaN.

    Parameters
    ----------
    series : pd.Series of raw text.

    Returns
    -------
    pd.Series of cleaned text strings.
    """
    return (
        series.fillna("")
        .astype(str)
        .str.lower()
        .str.strip()
        .str.replace(r"\s+", " ", regex=True)
    )


