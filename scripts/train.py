def get_similar_products(product_id: str, top_n: int = 5) -> list[dict]:
    """
    Backend inference helper — retrieve top-N similar products by TF-IDF cosine.

    Parameters
    ----------
    product_id : Target product identifier.
    top_n      : Number of similar products to return.

    Returns
    -------
    List of dicts: [{'product_id': ..., 'product_name': ..., 'similarity': ...}]
    """
    try:
        sim_matrix  = pickle.load(open(MODELS_DIR / "cosine_similarity_matrix.pkl", "rb"))
        id_map      = pickle.load(open(MODELS_DIR / "product_id_index.pkl", "rb"))
        product_ids = id_map["product_ids"]
        names_map   = {pid: name for pid, name in zip(id_map["product_ids"], id_map["product_names"])}

        if product_id not in product_ids:
            return []

        idx  = product_ids.index(product_id)
        sims = sim_matrix[idx].toarray().flatten()
        top  = np.argsort(sims)[::-1][1 : top_n + 1]

        return [
            {
                "product_id":   product_ids[i],
                "product_name": names_map.get(product_ids[i], ""),
                "similarity":   round(float(sims[i]), 4),
            }
            for i in top
        ]
    except Exception as exc:
        logger.warning(f"  get_similar_products error: {exc}")
        return []


