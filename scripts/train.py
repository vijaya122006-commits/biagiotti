def compute_safety_score(ingredient_text: str) -> Tuple[float, List[dict]]:
    """
    Compute a safety score (0–100) for a product based on its ingredient list.
    100 = perfectly safe (no known harmful ingredients detected).

    Parameters
    ----------
    ingredient_text : Raw ingredient string for one product.

    Returns
    -------
    (safety_score, list_of_detected_harmful_ingredients)
    """
    if not ingredient_text or str(ingredient_text).lower() in ("nan", ""):
        return 100.0, []

    text = str(ingredient_text).lower()
    found: List[dict] = []
    total_severity = 0

    for keyword, info in HARMFUL_KEYWORDS.items():
        if keyword in text:
            found.append(
                {
                    "keyword":  keyword,
                    "name":     info["name"],
                    "reason":   info["reason"],
                    "severity": info["severity"],
                }
            )
            total_severity += info["severity"]

    # Cap deduction: max penalty = 100, each severity point = 4 points deducted
    penalty = min(total_severity * 4, 100)
    score = max(0.0, 100.0 - penalty)
    return round(score, 1), found


