def predict_sentiment(review_text: str) -> dict:
    """
    Backend inference helper — analyse sentiment of a review string.

    Parameters
    ----------
    review_text : Review text.

    Returns
    -------
    dict: {'sentiment': str, 'compound': float, 'scores': dict}
    """
    try:
        payload = pickle.load(open(MODELS_DIR / "sentiment_model.pkl", "rb"))
        mode    = payload.get("mode", "vader")

        if mode == "classifier":
            pipeline = payload["pipeline"]
            label    = pipeline.predict([review_text])[0]
            proba    = pipeline.predict_proba([review_text])[0]
            return {
                "sentiment":  label,
                "mode":       "classifier",
                "confidence": round(float(max(proba)), 4),
            }
        elif mode == "vader":
            analyzer = payload["analyzer"]
            scores   = analyzer.polarity_scores(review_text)
            return {
                "sentiment": _vader_label(scores["compound"]),
                "compound":  round(scores["compound"], 4),
                "scores":    {k: round(v, 4) for k, v in scores.items()},
                "mode":      "vader",
            }
        else:
            from textblob import TextBlob
            blob = TextBlob(review_text)
            pol  = blob.sentiment.polarity
            return {
                "sentiment": "positive" if pol > 0.05 else ("negative" if pol < -0.05 else "neutral"),
                "polarity":  round(pol, 4),
                "mode":      "textblob",
            }
    except Exception as exc:
        logger.warning(f"  predict_sentiment error: {exc}")
        return {"sentiment": "unknown"}


