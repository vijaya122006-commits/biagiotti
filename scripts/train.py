def train_ingredient_similarity(df: pd.DataFrame) -> dict:
    """
    Build a TF-IDF vectorizer over ingredient text and compute the full
    cosine-similarity matrix.  Also exports a product_id → index mapping.

    Parameters
    ----------
    df : master_cleaned DataFrame. Must contain columns:
         'product_id', 'product_name', and either 'ingredients' or
         'ingredients_clean'.

    Returns
    -------
    dict with keys 'vectorizer', 'sim_matrix', 'product_ids', 'metrics'.
    """
    log_section("MODEL 1 — Ingredient Similarity (TF-IDF + Cosine)")
    t0 = time.time()

    # ── choose best ingredient column
    ing_col = "ingredients_clean" if "ingredients_clean" in df.columns else "ingredients"
    logger.info(f"  Using ingredient column: '{ing_col}'")

    subset = df[["product_id", "product_name", ing_col]].drop_duplicates(
        subset="product_id"
    )
    subset = subset.dropna(subset=["product_id"])

    # Clean text
    subset = subset.copy()
    subset["ing_text"] = clean_text(subset[ing_col])

    # Drop rows with empty ingredient text
    non_empty = subset[subset["ing_text"].str.len() > 2]
    logger.info(
        f"  Products with ingredient text: {len(non_empty):,} / {len(subset):,}"
    )

