def main() -> None:
    """
    Orchestrate the full training pipeline.
    Loads all datasets once, then dispatches to each model training function.
    """
    args = parse_args()
    train_all  = "all" in args.models
    to_train   = AVAILABLE_MODELS if train_all else args.models

    log_section("COSMETIC MARKET INTELLIGENCE — ML TRAINING PIPELINE")
    logger.info(f"  Start time   : {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    logger.info(f"  Models       : {to_train}")
    logger.info(f"  Output dir   : {MODELS_DIR}")
    logger.info(f"  Data dir     : {DATA_DIR}")
    pipeline_start = time.time()

    # ── Load datasets ──────────────────────────────────────────────────────
    log_section("DATA LOADING")
    df_master  = load_csv(DATA_MASTER,  "master_cleaned")
    df_sales   = load_csv(DATA_SALES,   "master_sales_cleaned")
    df_reviews = load_csv(DATA_REVIEWS, "master_reviews_cleaned")

    all_metrics: dict = {}

    # ── Model 1: Ingredient Similarity ────────────────────────────────────
    if "similarity" in to_train:
        # Prefer master_ingredients if available
        ing_path = DATA_DIR / "master_ingredients_cleaned.csv"
        if ing_path.exists():
            df_ing = load_csv(ing_path, "master_ingredients_cleaned")
            # Merge into master if product_id present
            if "product_id" in df_ing.columns and "product_id" in df_master.columns:
                df_for_sim = df_master.copy()
                if "ingredients" not in df_for_sim.columns or df_for_sim["ingredients"].isna().mean() > 0.5:
                    logger.info("  Merging ingredients from master_ingredients_cleaned …")
                    df_for_sim = df_master.merge(
                        df_ing[["product_id", "ingredients"]].dropna(),
                        on="product_id",
                        how="left",
                        suffixes=("_old", ""),
                    )
            else:
                df_for_sim = df_master
        else:
            df_for_sim = df_master

        all_metrics["similarity"] = train_ingredient_similarity(df_for_sim)

    # ── Model 2: Harmful Detector ─────────────────────────────────────────
    if "harmful" in to_train:
        # Use reviews df (richer ingredient data) if available
        df_for_harm = df_reviews if not df_reviews.empty else df_master
        all_metrics["harmful"] = train_harmful_detector(df_for_harm)

    # ── Model 3: Skin Classifier ──────────────────────────────────────────
    if "skin" in to_train:
        all_metrics["skin"] = train_skin_classifier(df_master, df_reviews)

    # ── Model 4: Sentiment Analysis ───────────────────────────────────────
    if "sentiment" in to_train:
        all_metrics["sentiment"] = train_sentiment_model(df_reviews)

    # ── Model 5: Demand Forecasting ───────────────────────────────────────
    if "forecast" in to_train:
        all_metrics["arima"]    = train_arima(df_sales, top_n=5)
        all_metrics["rf_forecast"] = train_rf_forecast(df_sales)

