def _engineer_forecast_features(ts_series: pd.Series) -> pd.DataFrame:
    """
    Engineer time-series features for a univariate sales series.

    Features created:
      - month, week_of_year, quarter  (from index)
      - lag_1, lag_2, lag_4, lag_8    (lagged values)
      - rolling_mean_4                 (4-week rolling mean)
      - season                         (1–4 per quarter)
      - is_holiday_season              (flag for Q4)

    Parameters
    ----------
    ts_series : pd.Series with DatetimeIndex and numeric values.

    Returns
    -------
    pd.DataFrame of features with target column 'y'.
    """
    df = pd.DataFrame({"y": ts_series})
    df["month"]           = df.index.month
    df["week_of_year"]    = df.index.isocalendar().week.astype(int)
    df["quarter"]         = df.index.quarter
    df["season"]          = (df["quarter"] - 1) % 4 + 1
    df["is_holiday"]      = (df["quarter"] == 4).astype(int)
    df["lag_1"]           = df["y"].shift(1)
    df["lag_2"]           = df["y"].shift(2)
    df["lag_4"]           = df["y"].shift(4)
    df["lag_8"]           = df["y"].shift(8)
    df["rolling_mean_4"]  = df["y"].shift(1).rolling(4).mean()
    return df.dropna()


def train_rf_forecast(df_sales: pd.DataFrame) -> dict:
    """
    Train a global RandomForestRegressor for demand forecasting.
    Uses feature-engineered time-series data across all products.

    Parameters
    ----------
    df_sales : master_sales DataFrame.

    Returns
    -------
    dict with RMSE, MAE, R² metrics.
    """
    log_section("MODEL 5B — Random Forest Demand Forecasting")
    t0 = time.time()

    ts = _make_sales_ts(df_sales, freq="W")
    if ts.empty:
        logger.warning("  Time series is empty — RF Forecast skipped.")
        save_model(None, "rf_forecast_model.pkl", "RF Forecast (empty)")
        return {}

    # Build global feature table across all product columns
    frames: List[pd.DataFrame] = []

    cols = ts.columns if ts.shape[1] > 1 else ts.columns
    for col in cols:
        feats = _engineer_forecast_features(ts[col])
        frames.append(feats)

    if not frames:
        logger.warning("  No engineered feature frames — RF Forecast skipped.")
        return {}

    combined = pd.concat(frames, ignore_index=True)
    combined = combined.replace([np.inf, -np.inf], np.nan).dropna()

    feature_cols = [c for c in combined.columns if c != "y"]
    X = combined[feature_cols].values
    y = combined["y"].values

    # Train/Test split (chronological)
    split = int(len(combined) * 0.8)
    X_train, X_test = X[:split], X[split:]
    y_train, y_test = y[:split], y[split:]

    logger.info(f"  Feature matrix : {combined.shape[0]:,} rows × {len(feature_cols)} features")
    logger.info(f"  Train/Test     : {len(X_train):,} / {len(X_test):,}")
    logger.info(f"  Features       : {feature_cols}")

    rf = RandomForestRegressor(
        n_estimators=200,
        max_depth=12,
        min_samples_split=5,
        min_samples_leaf=3,
        max_features="sqrt",
        n_jobs=-1,
        random_state=42,
    )
    logger.info("  Fitting Random Forest …")
    rf.fit(X_train, y_train)

    y_pred = rf.predict(X_test)
    rmse   = float(np.sqrt(mean_squared_error(y_test, y_pred)))
    mae    = float(np.mean(np.abs(y_test - y_pred)))
    r2     = float(r2_score(y_test, y_pred))

    logger.info(f"\n  ── RF Forecast Evaluation ──")
    logger.info(f"  RMSE  : {rmse:.4f}")
    logger.info(f"  MAE   : {mae:.4f}")
    logger.info(f"  R²    : {r2:.4f}")

    # Feature importances
    importances = sorted(
        zip(feature_cols, rf.feature_importances_),
        key=lambda x: x[1],
        reverse=True,
    )
    logger.info("\n  Feature Importances:")
    for fname, imp in importances:
        bar = "█" * int(imp * 40)
        logger.info(f"    {fname:<20s}  {imp:.4f}  {bar}")

    save_model(
        {"model": rf, "feature_cols": feature_cols},
        "rf_forecast_model.pkl",
        "Random Forest Demand Forecast",
    )

    metrics = {
        "rmse":           round(rmse, 4),
        "mae":            round(mae, 4),
        "r2":             round(r2, 4),
        "n_features":     len(feature_cols),
        "n_estimators":   rf.n_estimators,
        "elapsed":        elapsed(t0),
    }
    logger.info(f"\n  ✔ RF Forecast complete  ({elapsed(t0)})")
    return metrics


