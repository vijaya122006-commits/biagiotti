def load_pkl(key: str) -> Optional[Any]:
    """
    Load a pickle artefact by symbolic key.

    Parameters
    ----------
    key : Key from ARTEFACTS dict.

    Returns
    -------
    Deserialized object or None if file missing.
    """
    path = ARTEFACTS.get(key)
    if path is None:
        print(C.warn(f"Unknown artefact key: '{key}'"))
        return None
    if not path.exists():
        print(C.warn(f"File missing: {path.name}"))
        return None
    try:
        t0  = time.time()
        obj = pickle.load(open(path, "rb"))
        ms  = (time.time() - t0) * 1000
        size_kb = path.stat().st_size / 1024
        print(C.kv(f"  Loaded {path.name}:", f"{size_kb:.1f} KB  in {ms:.0f} ms"))
        return obj
    except Exception as exc:
        print(C.fail(f"Failed to load {path.name}: {exc}"))
        return None


