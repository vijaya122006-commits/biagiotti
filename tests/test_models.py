def test_rf_forecast() -> None:
    print(C.sub("Random Forest Demand Forecast"))
    payload = load_pkl("rf_forecast_model")
    if payload is None:
        suite.skip("RF Forecast", "file missing")
        return

    try:
        rf           = payload["model"]
        feature_cols = payload["feature_cols"]

        print(C.kv("  n_estimators:", rf.n_estimators))
        print(C.kv("  max_depth:", rf.max_depth))
        print(C.kv("  Feature columns:", feature_cols))

        # Build 7-step forecast
        import pandas as pd
        history  = list(SAMPLE_FORECAST_HISTORY)
        steps    = 7
        ref_date = pd.Timestamp("2025-09-01")
        forecast: list[float] = []

        for step in range(steps):
            future = ref_date + pd.DateOffset(weeks=step + 1)
            row = {
                "month":          future.month,
                "week_of_year":   future.isocalendar()[1],
                "quarter":        future.quarter,
                "season":         (future.quarter - 1) % 4 + 1,
                "is_holiday":     int(future.quarter == 4),
                "lag_1":          history[-1],
                "lag_2":          history[-2] if len(history) >= 2 else history[-1],
                "lag_4":          history[-4] if len(history) >= 4 else history[-1],
                "lag_8":          history[-8] if len(history) >= 8 else history[-1],
                "rolling_mean_4": float(np.mean(history[-4:])),
            }
            x    = np.array([[row[c] for c in feature_cols]])
            pred = float(max(0.0, rf.predict(x)[0]))
            forecast.append(round(pred, 2))
            history.append(pred)

        # Visualise as sparkline
        mn, mx = min(forecast), max(forecast)
        span   = mx - mn if mx != mn else 1.0
        spark  = "".join(
            ["▁▂▃▄▅▆▇█"[min(7, int(7 * (v - mn) / span))] for v in forecast]
        )

        print()
        print(f"  {C.BOLD}7-week demand forecast (starting 2025-09-01):{C.RESET}")
        for w, val in enumerate(forecast, 1):
            bar = "█" * min(40, int(val / max(forecast) * 40))
            print(f"    Week {w}:  {C.GREEN}{val:8.2f}{C.RESET}  {C.BLUE}{bar}{C.RESET}")

        print()
        print(C.kv("  Sparkline:", f"{C.CYAN}{spark}{C.RESET}"))
        print(C.kv("  Min / Max:", f"{mn:.2f} / {mx:.2f}"))
        trend = "↑ rising" if forecast[-1] > forecast[0] else ("↓ falling" if forecast[-1] < forecast[0] else "→ flat")
        print(C.kv("  Trend:", trend))
        print()
        print(C.result("Forecast",
                       f"[{', '.join(str(v) for v in forecast[:4])} …]"))
        suite.ok("RF Forecast", f"7-week  min={mn:.1f} max={mx:.1f}")

    except Exception as exc:
        suite.fail("RF Forecast", str(exc))
        if VERBOSE:
            traceback.print_exc()


